				<h1 style="margin-top: 0pt;">Hand Held Automatic Feed Screwdriver</h1>

				<h2>Design & Service</h2>

				The Design Tool team has a combined 85 years of experience in the design and manufacturing of automatic screw driving equipment and vast AutoCAD knowledge. Each DTI 5000 is designed specifically to the customer’s fastener and application. The DTI 5000 can be designed with pistol grip or straight type air screwdrivers with various rpm’s and torque, with reverse capabilities.

				DTI can also build on any brand of screwdriver.

