				<h2 style="margin-top: 0pt;">Efficient</h2>

				The simplicity of the patented feed mechanism makes this screw feeder less likely to experience mechanical failure. The twist-lock feed cover permits fast access to the feed mechanism allowing it to be disassembled and reassembled in seconds without the use of tools. This simple design, in conjunction with the twist-lock feed cover, exposes the screws moving through the feed mechanism allowing quick visible detection and accessibility should a jam occur because of a foreign object or a defective screw. The automatic timed shut off feature on the DTI 5000 base unit reduces wear on the feeder bowl and makes the unit more efficient. The machine automatically shuts off if unit is not cycled for 7-8 seconds and restarts when the operator drives another screw.<br/>

				<br/>

				<h2>Ease Of Operation</h2>

				<p>Our unique barrel design allows the DTI 5000 to be much smaller in size and lighter in weight in comparison to other machines. The patented barrel design allows flexibility to position the entrance tube anywhere on the barrel and sleeve assembly. This design enables the DTI 5000 to be used in more applications than other units on the markets today. </p>



